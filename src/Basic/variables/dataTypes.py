age = 10 # Integer
size = 1.80 # Float
name = "James" # String
isStudent = True # Boolean

print(type(age))
print(type(size))
print(type(name))
print(type(isStudent))

# int to float
result = float(age)

print(result)
print(type(result))

#float to int
result = int(size)

print(result)
print(type(result))

# bool to str
result = str(isStudent)

print(result)
print(type(result))

# bool to int

result = int(isStudent) # 1 = True, 0 = False

print(result)
print(type(result))