# EXAMPLE

x = 60
y = 60

if x > y: # 1. koşul - X, Y'den büyük ise bu blok çalışır
    print("X, Y'den büyüktür")

elif x == y: # 2. koşul - X ile Y eşit ise bu blok çalışır
    print("X ile Y eşittir")

else: # Koşullar sağlanmaz ise - 2 koşul da sağlanmıyorsa bu blok çalışır
    print("Y, X'den büyüktür")



